void solve() {
   int n;
   cin >> n;
   int ans=1001;
   while(n){
        ans=min(ans,n%10);
        n/=10;
   }
   cout<<ans<<'\n';
}
 
int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    
    int t;
    std::cin >> t;
    
    while (t--) {
        solve();
    }
    
    return 0;
}